namespace Velentr.Collisions.ShapeDefinitions
{
    /// <summary>
    ///     Values that represent shapes.
    /// </summary>
    public enum Shape
    {

        /// <summary>
        ///     An enum constant representing the circle option.
        /// </summary>
        Circle,

        /// <summary>
        ///     An enum constant representing the rectangle option.
        /// </summary>
        Rectangle,

        /// <summary>
        ///     An enum constant representing the pixel rectangle option.
        /// </summary>
        PixelRectangle,

        /// <summary>
        ///     An enum constant representing the point option.
        /// </summary>
        Point,
    }
}
